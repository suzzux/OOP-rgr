/**
 * Інтерфейс для патерну Command. Визначає єдиний метод для виконання операції.
 * Це дозволяє інкапсулювати запит як об'єкт, параметризувати клієнтів
 * різними запитами, ставити запити в чергу та підтримувати їх скасування.
 */
public interface Command {
    /**
     * Виконує команду.
     * @throws VendingMachineException якщо під час виконання команди виникає помилка.
     */
    void execute() throws VendingMachineException;
}
