/**
 * Інтерфейс для патерну Strategy. Визначає метод оплати.
 * Це дозволяє інкапсулювати різні алгоритми оплати (готівкою, карткою)
 * та змінювати їх незалежно від клієнтського коду.
 */
public interface PaymentStrategy {
    /**
     * Метод для здійснення оплати.
     * @param amount Сума до сплати.
     * @return true, якщо оплата пройшла успішно, інакше false.
     */
    boolean pay(double amount);
}
